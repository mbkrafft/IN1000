""" Dette programmet summerer først to heltall,
så setter sammen to tekststringer. """

# lager to variabler med heltall
variabelEn = 5
variabelTo = 6

# printer ut verdiene til variablene hver for seg
print (variabelEn)
print (variabelTo)

# summerer variablene og printer ut resultatet
summert = variabelEn + variabelTo
print (f'Sum: {summert}')

# lager to variabler med tekststringer
tekstEn = "Hei du din luring!"
tekstTo = "Hvem er du?"

# printer ut variablene hver for seg
print (tekstEn)
print (tekstTo)

# setter sammen tekststringene i variablen 'sammen' og printer ut resultatet
sammen = tekstEn + tekstTo
print (sammen)

# endrer variablen 'sammen' til å ha mellomrom mellom tekststringene og
# printer ut resultatet
sammen = tekstEn + ' ' + tekstTo
print (sammen)
